# 1 ate o 5
print("\n")
for numero in range(1, 6):
    print(numero)

# 5 ate o 1
print("\n")
for numero in range(5, 0, -1):
    print(numero)

# 5 + 4 + 3 + 2 + 1
print("\n")
soma = 0
for numero in range(1, 6):
    soma = soma + numero
    print(soma)

# S O R V E T E
print("\n")
palavra = "sorvete"
for letra in palavra:
    print(letra)

# For dentro de For
print("\n")
for i in range(0, 5):
    print(i)
    print("---")
    for j in range(0, 3):
        print(j)
    print()
