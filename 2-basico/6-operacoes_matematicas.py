import math

a = 50
b = 3

print("A:", a)
print("B:", b)

soma = a + b
print("Soma:", soma)

sub = a - b
print("Subtração:", sub)

div = a / b
print("Divisão:", div)

mult = a * b
print("Multiplicação:", mult)

resto = a % b
print("Resto:", resto)

elevado = a ** b
print(a, " elevado a ", b, "é: ", elevado)
# 50 * 50 * 50

raizquadrada = math.sqrt(b)
print("Raiz quadrada de ", b, "é:", raizquadrada)

arredondamento =  round(raizquadrada, 2)
print("Arredondamento:", arredondamento)
